function interpolate(str, vars) {
  if (!str || typeof str !== 'string') return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : `{${k}}`));
}

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('hasm_lang') || 'ar');

  useLayoutEffect(() => {
    try {
      localStorage.setItem('hasm_lang', lang);
    } catch (_) {}
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
    const title =
      translations[lang]?.documentTitle ??
      translations.ar.documentTitle ??
      translations.en.documentTitle;
    if (title) document.title = title;
    const metaDesc =
      lang === 'ar'
        ? 'حسم — سوق تسعير ديناميكي. تنخفض الأسعار بشكل مباشر.'
        : 'Hasm — dynamic pricing marketplace. Prices drop in real time.';
    let meta = document.querySelector('meta[name="description"]');
    if (!meta) {
      meta = document.createElement('meta');
      meta.setAttribute('name', 'description');
      document.head.appendChild(meta);
    }
    meta.setAttribute('content', metaDesc);
  }, [lang]);

  const resolve = (key) =>
    translations[lang]?.[key] ??
    translations.ar[key] ??
    translations.en[key] ??
    key;

  const t = (key) => resolve(key);

  const tf = (key, vars = {}) => interpolate(String(resolve(key)), vars);

  return (
    <LanguageContext.Provider value={{ lang, setLang, t, tf, isRTL: lang === 'ar' }}>
      {children}
    </LanguageContext.Provider>
  );
}

export const useLanguage = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useLanguage must be used within LanguageProvider');
  return ctx;
};
